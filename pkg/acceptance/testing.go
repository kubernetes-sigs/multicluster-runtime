package acceptance

import "context"

// TestingT is the minimal subset of testing.TB needed for acceptance
// tests.
type TestingT interface {
	Context() context.Context
	TempDir() string
	Helper()
	Cleanup(func())
	Log(args ...any)
	Logf(format string, args ...any)
	Error(args ...any)
	Errorf(format string, args ...any)
	Fatal(args ...any)
	Fatalf(format string, args ...any)
}
